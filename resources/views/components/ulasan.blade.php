<script>
document.addEventListener("DOMContentLoaded", function () {
  const container = document.getElementById("testimonial-container");
  const prevBtn = document.getElementById("prev-btn");
  const nextBtn = document.getElementById("next-btn");
  const items = document.querySelectorAll(".testimonial-item");
  
  if (!container || !prevBtn || !nextBtn || items.length === 0) {
    console.error("Elemen testimoni tidak ditemukan.");
    if(prevBtn) prevBtn.style.display = 'none';
    if(nextBtn) nextBtn.style.display = 'none';
    return;
  }

  let currentIndex = 0;
  let totalItems = items.length;

  // PENYESUAIAN JAVASCRIPT: Mengubah logika untuk menampilkan 3 item di layar besar
  function getVisibleItems() {
    if (window.innerWidth >= 1024) return 3; // 3 item untuk layar lg (desktop)
    return 1; // 1 item untuk layar di bawahnya (tablet & mobile)
  }
  
  function updateCarousel() {
    const visibleItems = getVisibleItems();
    if (!container.parentElement) return;

    // Perhitungan lebar item berdasarkan parent dan gap
    const gap = parseInt(window.getComputedStyle(container).gap) || 24; // 24px adalah gap-6
    const totalGapWidth = gap * (visibleItems - 1);
    const itemWidth = (container.parentElement.offsetWidth - totalGapWidth) / visibleItems;
    const scrollAmount = (itemWidth + gap) * currentIndex;

    container.style.transform = `translateX(-${scrollAmount}px)`;

    // Mengatur visibilitas tombol
    prevBtn.style.display = currentIndex > 0 ? "flex" : "none";
    nextBtn.style.display = currentIndex < totalItems - visibleItems ? "flex" : "none";
  }

  nextBtn.addEventListener("click", () => {
    const visibleItems = getVisibleItems();
    if (currentIndex < totalItems - visibleItems) {
      currentIndex++;
      updateCarousel();
    }
  });

  prevBtn.addEventListener("click", () => {
    if (currentIndex > 0) {
      currentIndex--;
      updateCarousel();
    }
  });

  window.addEventListener("resize", () => {
    const visibleItems = getVisibleItems();
    // Pastikan currentIndex tidak keluar dari batas saat resize
    if (currentIndex > totalItems - visibleItems) {
      currentIndex = Math.max(0, totalItems - visibleItems);
    }
    updateCarousel();
  });

  // Panggil pertama kali untuk inisialisasi
  updateCarousel();
});
</script>